/**
 * @param {string | null | undefined} venue
 * @param {string | null | undefined} raceName
 * @returns {string | null}
 */
export function guessRegion(venue, raceName) {
  const combined = `${String(venue ?? "")} ${String(raceName ?? "")}`.replace(/\s+/g, " ").trim();
  if (!combined) return null;

  for (const [needle, region] of NEEDLE_TO_REGION) {
    if (combined.includes(needle)) return region;
  }

  for (const hint of SEOUL_VENUE_HINTS) {
    if (combined.includes(hint)) return "서울";
  }

  return null;
}
